function add(num1, num2) {
    return num1 + num2
}

function subtract(num1, num2) {
    return num1 - num2;
}

function multiply(num1, num2) {
    return num1*num2;
}

function divide(num1, num2){
    return num1/num2;
}

console.log(add(3,4))
console.log(subtract(3,4))
console.log(multiply(3,4))
console.log(divide(3,4))
